    // ðŸ”¹ Method to read customer details from file
    private Customer getCustomerDetails(String username, String password, String filePath) {
    try (BufferedReader reader = new BufferedReader(new FileReader(filePath))) {
        String line;
        while ((line = reader.readLine()) != null) {
            // Assuming file format: ID,username,password,fullname,email,phone,address
            String[] parts = line.split(",");
            if (parts.length >= 7) {
                if (parts[1].equals(username) && parts[2].equals(password)) {
                    return new Customer(parts[0], parts[1], parts[2],
                                        parts[3], parts[4], parts[5], parts[6]);
                }
            }
        }
    } catch (IOException e) {
        e.printStackTrace();
    }
    return null;
}

